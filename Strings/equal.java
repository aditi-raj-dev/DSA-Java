package Strings;

public class equal {
    public static void main(String[] args) {
        String str1 = "Hello, World!";
        String str2 = "Hello, World!";

        // Check if the two strings are equal
        if (str1.equals(str2)) {
            System.out.println("The strings are equal.");
        } else {
            System.out.println("The strings are not equal.");
        }
    }

}
